class NestedIterator(object):

    def __init__(self, nestedList):
        self.nl = nestedList[::-1]

    def next(self):
        return self.nl.pop().getInteger()

    def hasNext(self):
        while self.nl and not self.nl[-1].isInteger():
            self.nl.extend(self.nl.pop().getList()[::-1])
        return bool(self.nl)


